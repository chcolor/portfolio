<h1> Christine's Intro to Digital Media Website!</h1>

<p>Hi! You have found your way to my Digital Media Website! This is where I post assignments for Art 74: Intro to Digital Media! Exciting huh? I am currently pursuing my Bachelors degree in Digital Media Art at San Jose State University in San Jose, California. Previously I have attended California State University East Bay, the University of Central Florida, and Valencia Community College.</p>

<p>I am originally from Orlando, Florida. I moved to the San Francisco Bay area in 2011 and fell in love with the city, the hills, the coast, the trees and most of all the people and the diverse cultures they bring to the area.</p>

<p>As far as my art goes, I have dabbled in almost any art form I can think of including photography, painting, drawing, printmaking, industrial design/architecture, ceramics, collage, folk art, jewelry making, embroidery, crochet, sewing, and glassblowing. However, I consider myself to be a Sculptor at heart because I feel most at home when I am swinging a hammer or working with clay. I am pursuing a degree in Digital Media in the hopes that I can translate my love for 3-D creation to an electronic medium be that through animation, 3-D printing, or automatic(moving) sculpture.</p>


<p>Enjoy!</p>

