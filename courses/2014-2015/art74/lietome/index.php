 <div> <p class="txtblk center">This piece, a poster for Exclusion Zone National Park, was created in response to a class project entitled “Lie to me.” The objective of this project was to produce a series of images created with the intent to fool the user into believing that the product exists and these materials are a representation of the product. I chose to create Exclusion Zone National Park because of my fascination with the nuclear disaster in Chernobyl, Ukraine in 1986. This is an area that has been abandoned by civilization due to the dangers of radiation exposure. With the passing of time, however, it has become a relatively safe place to visit for short periods of time and creates a unique experience for tourists, allowing them to explore an abandoned modern city.
 </p><p class="txtblk center">The style I chose for the poster is a classic style from the 1930’s common to WPA Posters for US National Parks. I thought this a fitting genre because American propaganda art from the early to mid 1900’s produced by the WPA was very similar in style and mission to Russian propaganda art prodced for the Communist party during that timeframe. </p><p class="txtblk center">While producing the poster I encountered many difficulties. It was created using Adobe Illustrator and Photoshop. I have used both programs previously but not extensively. The first challenge I confronted was how to transform my reference images into simple forms that would resemble something WPA artists might have created. I started by choosing a limited color palette and tracing the outlines of the buildings. I added perspective by adding 20% black to the colors on the opposite faces of the buildings. I put windows in by creating the first level using the line and pen tools and copying this level as many times as needed. The trees were created using an open source tree vector that I altered to fit my color scheme. I created a different brush for each of the 3 different tree designs found in the artwork. Starting from the horizon line, I drew lines across the artwork and applied the different brushes to them, creating new brushes with larger trees as the lines got closer to the foreground of the image. After studying the text on the original WPA posters I searched for a font that closely matched the style originally used. I used the character palette to adjust the horizontal scaling and to make the font bolder or lighter depending on the use. In order to place the National Parks Logo I opened an original WPA poster in Photoshop and selected the logo area. I pasted this into my artwork and adjusted the color balance in the Edit menu. Lastly, I added the border by creating a round-cornered rectangle of the desired size and placed all the other layers into a group underneath it. I then made the rectangle into a clipping mask.</p>
 
 
