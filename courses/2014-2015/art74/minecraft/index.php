<p class="txtblk center">I felt that Minecraft would be a great medium for this project because of its versatility and ease of access to its users. Minecraft contains many different building blocks for the user to choose from. In creative mode the user is given access to all available materials. This, combined with the power of the World Edit add-on, allowed me to create the landscape in many different colors and textures. Below is a picture of some of the different blocks available to builders and the codes needed to use them with the World Edit tool.</p>

<img class="center" src="images/m1dataval.png" alt="worldedit" width="700">

