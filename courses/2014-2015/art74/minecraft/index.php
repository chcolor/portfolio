<p class="txtblk center">This project had a very broad prompt which basically stated that we should make something that we consider to be art utilizing some form of digital media in the process. I decided to recreate a painting in Minecraft. The painting I chose was "Autumn in France" by Emily Carr.</p>

<p class="txtblk center">As I was thinking of ideas for this project I became increasingly interested in how technology is now allowing us to create new and interesting virtual landscapes. Restrictions are being lifted every day in regards to what we can create. I thought this would be a wonderful opportunity to both pay homage to one of my favorite painters as well as be able to experience her art in a new and interesting way. Finally I would be able to take a stroll thorugh one of Emily Carr's fascinating landscapes.</p>

<p class="txtblk center">The reason I chose this artist and painting, besides my infatuation with the artist and her work, is because she uses color in very interesting ways which I thought would lend itself nicely to recreation with the vivid materials available in Minecraft. Her paintings are often surrealistic which I thought would be interesting to reinterpret inside the linear structure this medium is limited to.</p>

<p class="txtblk center">Editing the Minecraft world was made easier by using World Edit, a Minecraft add-on that allows you to edit the environment with command line codes. For more information on this, click the picture below. This will take you to the website where it can be downloaded as well as information about the functionality of the tool.</p>

<a href="http://wiki.sk89q.com/wiki/WorldEdit" target="_blank"><img class="center" src="images/m2worldedit.png" alt="worldedit" width="700"></a>

<p class="txtblk center">In order to start with a clean slate, I entered Minecraft's Super Flat World Customizer. I noticed that each block seemed to have different values associated with it so I searched the web for more information on customizing Minecraft worlds. I found that the user can alter the material that the world is made of simply by changing the hex code and quantity values for the world layers. Here is a video showing that process. </p>
 <video class="center" height="450" controls>
  <source src="video/create.mp4" type="video/mp4">
